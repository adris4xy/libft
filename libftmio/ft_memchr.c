#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ptr;
	unsigned char	c2;
	int				i;

	i = 0;
	c2 = (unsigned char)c;
	ptr = (unsigned char *)s;
	while (n--)
	{
		if (ptr[i] == c2)
			return (ptr + i);
		i++;
	}
	return (NULL);
}
//Localiza la primera aparicion de 'c'(convertido a unsigned char) en la string s.
//Devuelve un puntero al byte donde se ubica 'c' o NULL si no existe 'c' dentro de 'n' bytes.